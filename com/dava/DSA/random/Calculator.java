public class Calculator {
  int a;
  int b;
  Calculator(int a, int b) {
    this.a = a;
    this.b = b;
  }
  int add(int a, int b) {
    return a + b;
  }
  int sub(int a, int b) {
    return a - b;
  }
  int multi(int a, int b) {
    return a * b;
  }
  int divide(int a, int b) {
    return a / b;
  }
}
